              <p>Ключевой параметр в выборе мышки – это конечно же ее форма и то как она лежит у вас в руке. Форма мышек может быть симметричной, либо отформована под правшей или левшей. Вес так же не малозначительный параметр при выборе мышки, от него зависит ваша скорость и точность управления. Стоит отметить, что в некоторых современных мышках вес можно регулировать</p>
