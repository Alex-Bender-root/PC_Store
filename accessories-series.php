          <div class="selectio_tips_content">
            <h6>Форма корпуса, вес</h6>
            <p>Ключевой параметр в выборе мышки – это конечно же ее форма и то как она лежит у вас в руке. Форма мышек может быть симметричной, либо отформована под правшей или левшей. Вес так же не малозначительный параметр при выборе мышки, от него зависит ваша скорость и точность управления. Стоит отметить, что в некоторых современных мышках вес можно регулировать</p>
          </div>
          <div class="selectio_tips_content">
            <h6>Тип сенсора</h6>
            <p>Если между оптическим и лазерным сенсором вопрос уже решен в пользу последнего, то вот на разрешение DPI стоит обратить внимание. Особенно если вы используете монитор с разрешением 4к или мульти-мониторную конфигурацию. Чем выше частота опроса сенсора, тем более точно и правильно вы сможете настроить под себя движение курсора на вашем экране</p>
          </div>
          <div class="selectio_tips_content">
            <h6>Тип подключения</h6>
            <p>Проводная или беспроводная мышь, является не менее важным выбором. С одной стороны комфорт, с другой стабильность и скорость отклика. Формула проста – для геймеров проводные мышки, для других задач комфортнее будут беспроводные. Стоит отметить, что некоторые топовые беспроводные мышки имеют не менее быструю скорость передачи данных как и у проводных версий</p>
          </div>
          <div class="selectio_tips_content">
            <h6>Дополнительные кнопки, софт</h6>
            <p>Дополнительные кнопки нужны как для вспомогательных функций, так и для записи макросов, то есть сочетаний клавиш. Как в играх, особенно в MMORPG, так и в различных профессиональных программах, это позволяет неплохо экономить время и быть впереди своих соперников. С помощью специального софта можно настраивать любые комбинации клавиш, а так же несколько профилей для разных задач</p>
